"""
4. Crie um algoritmo que leia três números some 5 aos seus valores e mostre os
valores resultantes maiores que 10. 
"""

x = int(input("Digite um numero"))
y = int(input("Digite um numero"))
z = int(input("Digite um numero"))
a = x + 5
b = y + 5
c = z + 5
if(a>10):
    print(a)
if(b>10):
    print(b)
if(c>10):
    print(c)
